
# aça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.

# peça ao promp a medida da base e da altura
x = float(input("Medida da base: "))
y = float(input("Medida da altura: "))

#formula para medição da area de um quadrado
formula = (x * y)

# imprima o valor para o promp
print(f"A area de um quadrado é:{formula}")
